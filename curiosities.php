            <h1>10 Curiosities About Art </h1>
        </div>
        <div class="art1">

            <img src="Fotot/art1.png" width="800" height="500">
            <h2>1. Mona Lisa</h2>
            <p class="mona">While some that claim <mark>Leonardo da Vinci</mark>’s most famous painting is a self-portrait of the artist himself in drag, research has concluded it is likely a portrait of a woman named Lisa Gherardini, a member of a prominent Florentine family and wife of a wealthy silk merchant. Leonardo’s father allegedly knew Gherardini’s father very well, and the painting was possibly commissioned by him.</p>

            <h2>2. The Last Supper</h2>
            <img src="Fotot/art2.jpg" width="600" height="340">
            <p class="mona">
                Da Vinci’s other most famous work—which can be seen in the Convent of Santa Maria delle Grazie in <mark>Milan</mark>, Italy—originally included Jesus’ feet. But in 1652, while installing a doorway in the refectory where the painting is on view, builders cut into the bottom-center of the mural, lopping off Jesus’ feet.
            </p>

            <h2>3. The Starry Night</h2>
            <img src="Fotot/art3.jpg" width="600" height="400">
            <p class="mona">
                The small town depicted in Vincent van Gogh’s The Starry Night is Saint-Rémy-de-Provence in the south of France. Van Gogh painted the work while he was a patient at the Saint-Paul-de-Mausole, a psychiatric hospital in Saint-Rémy. Presently, the hospital has a wing named after the painter.
                <h2>4. Michelangelo's David</h2>
                <img src="Fotot/art4.png" width="600" height="400">
                <p class="mona">
                    The marble slab that was eventually turned into the sculpture of David by Michelangelo in 1504 was cut 43 years earlier for an artist named Agostino di Duccio, who planned to turn it into a statue of Hercules. Di Duccio abandoned his sculpture, which was originally to be installed in a Florentine cathedral, and the marble was unused for 10 years until another sculptor, named <mark>Antonio Rossellino</mark>, decided to work with it. Rossellino also abandoned his work because he found marble too difficult to sculpt, and eventually Michelangelo began work on his sculpture in 1501.
                </p>

            <h2>5. The Creation of Adam</h2>
            <img src="Fotot/art5.jpg" width="600" height="300">
            <p class="mona">Michelangelo painted the fresco ceiling of the Sistine Chapel—including the most famous panel called “The Creation of Adam,” which depicts God giving life to the first man—entirely standing up. The artist invented a series of scaffolds specially designed to attach to the chapel walls with brackets so he and his assistants could be close enough to the ceiling to reach above their heads to work and paint.</p>

            <h2>6. The Scream</h2>
            <img src="Fotot/art6.png" width="600" height="400">
            <p class="mona">There are technically five separate versions of Expressionist artist <mark>Edvard Munch</mark>’s most famous work, The Scream. The first two, from 1893 and created with tempera and crayon on cardboard, are located in the National Gallery in Oslo and the Munch Museum, respectively. A privately owned third version created in 1895 with pastels recently sold for nearly $120 million at auction. Yet another version from 1895 is a black and white lithograph. A final version, done in 1910 by Munch due to the popularity of the previous incarnations, is also held in the Munch Museum, and it made headlines in recent years for being stolen in 2004 and recovered in 2006.</p>

            <h2>7. The Thinker</h2>
            <img src="Fotot/art7.png" width="600" height="400">
            <p class="mona">Though there are now dozens of casts of <mark>Auguste Rodin</mark>’s famous sculpture The Thinker around the world, it had a much smaller origin. Rodin originally created a 70cm version in 1880 as the central component to a bigger sculptural work called “The Gates of Hell.” Inspired by Dante’s Inferno, the piece—first called The Poet—was conceived as a representation of Dante himself. The re-dubbed sculpture was exhibited on its own in 1888, then was enlarged to the depiction we know it today in 1904.</p>

            <h2>8. Girl with a Pearl Earring</h2>
            <img src="Fotot/art8.png" width="600" height="400">
            <p class="mona">Much like the Mona Lisa, the subject of <mark>Johannes Vermeer</mark>’s Girl with a Pearl Earring has been highly contested—but for the most likely candidate, Vermeer didn’t have to look far. The model for his painting is thought to be his daughter Maria.</p>

            <h2>9. American Gothic</h2>
            <img src="Fotot/art9.png" width="600" height="400">
            <p class="mona">Another famous painting with interesting models is <mark>Grant Wood</mark>’s American Gothic, which can be seen on view in the collection of the Art Institute of Chicago. To depict—for better or worse—the ideals of rural America, Wood wanted to use his mother, Hattie, as a model for his painting. Wood determined that standing for so long would be far too exhausting for his mother, so he had his sister wear his mother’s apron and pin while posing. For the male subject in the painting, Wood used his 62-year-old dentist.</p>

            <h2>10. Nighthawks</h2>
            <img src="Fotot/art10.jpg" width="600" height="350">
            <p class="mona">Another painting in the collection of the Art Institute of Chicago is Edward Hopper’s Nighthawks. Hopper allegedly based the painting on a diner that was located in New York City’s Greenwich Village in an area where Greenwich Street meets 11th Street and 7th Avenue called Mulry Square. But he actually based the painting on an all-night coffee stand. “I simplified the scene a great deal and made the restaurant bigger,” he said. “Unconsciously, probably, I was painting the loneliness of a large city.”</p>
            
